uint32_t last_frame_time = 0;
uint8_t current_frame = 0;

void oled_update_animation(void) {
    uint32_t now = timer_read32();
    if (now - last_frame_time < 100) {
        return;
    }
    last_frame_time = now;

    oled_clear();
    oled_write_raw(oled_animation[current_frame], ANIM_FRAME_SIZE);

    current_frame = (current_frame + 1) % ANIM_FRAMES;
}

void oled_init_animation(void) {
    oled_clear();
    last_frame_time = timer_read32();
    current_frame = 0;
}
